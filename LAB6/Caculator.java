public class Caculator {
    public static int sum(int x, int y){//for sum of numbers
        return (x+y);
    }
    public static int multi(int x, int y){//for product of numbers
        return (x*y);
    }
    public static double div(double x, double y){//for division of numbers
        return (x/y);
    }
    
     public static int mod(int x, int y){//for modulus of numbers
        return (x%y);
    }
     public static double sin(double x){
        return (Math.sin(x));
    }
     public static double cos(double x){
        return (Math.cos(x));
    }
     public static double tan(double x){
        return (Math.tan(x));
    }
}

  